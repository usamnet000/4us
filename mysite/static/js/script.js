function setCookie(cname, cvalue, exdays) {
    var d=new Date();
    d.setTime(d.getTime() + (exdays*24*60*60*1000));
    var expires="expires="+ d.toUTCString();
    document.cookie=cname + "=" + cvalue + ";" + expires + ";path=/";
}

function getCookie(cname) {
    var name=cname + "=";
    var decodedCookie=decodeURIComponent(document.cookie);
    var ca=decodedCookie.split(';');
    for(var i=0;i <ca.length;i++) {
        var c=ca[i];
        while (c.charAt(0)==' ') {
            c=c.substring(1);
        }
        if (c.indexOf(name)==0) {
            return c.substring(name.length, c.length);
        }
    }
    return "";
}
$(function () {
    // Change Map TypeId
    function TypeIdChange ( option) {
        switch (option) {
            case "1": map.setMapTypeId( google.maps.MapTypeId.ROADMAP);
            break;
            case "2": map.setMapTypeId( google.maps.MapTypeId.TERRAIN);
            break;
            case "3": map.setMapTypeId( google.maps.MapTypeId.SATELLITE);
            break;
            case "4": map.setMapTypeId( "styled_map");
            break;
            default: map.setMapTypeId( "styled_map");
        }
    }
    $('input[type=radio][name=option]').change(function() {
        var self=$(this);
        TypeIdChange( self.val());
    }
    );
});
// Zoom control function
function ZoomControl ( map) {
    var zoomIn=document.getElementById('zoomIn');
    var zoomOut=document.getElementById('zoomOut');
    google.maps.event.addDomListener(zoomOut, 'click', function() {
        var currentZoomLevel=map.getZoom();
        if(currentZoomLevel !=0) {
            map.setZoom(currentZoomLevel - 1);
        }
    }
    );
    google.maps.event.addDomListener(zoomIn, 'click', function() {
        var currentZoomLevel=map.getZoom();
        if(currentZoomLevel !=21) {
            map.setZoom(currentZoomLevel + 1);
        }
    }
    );
}


